// 1 bit number true or false
_Bool thisIsFalse= 0; // false
_Bool thisIsTrue = 1; // true

// 8 bits, one byte
// %c
char x = 'A';

// 16 bits, two bytes
// %hd
short x = 16;

// 32 bits, four bytes
// %d
int x = 32;

// 32 or 64 bits depending if we compile with -m32 or -m63
// %ld
long x = 64;

// 64 bits, eight bytes
// %lu
unsigned long x = ~64;

// 128 bits, sixteen bytes
// %lld
long long x = 128;

// float
// %f
float x = 3.14;



